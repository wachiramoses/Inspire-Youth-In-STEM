f_num = 30
s_num = 50
b_num = 40
#addition
sum_num = f_num + s_num
print("The sum of the numbers is: ",sum_num)

#product
p_num = f_num * s_num
print("The product of the numbers is: ",p_num)

#difference
diff_num = f_num - s_num
print("The difference of the numbers is: ",diff_num)

#Quotient
q_num = f_num / s_num
print("The quotient of the numbers is: ",q_num)

#addition
sum_num = f_num + s_num + b_num

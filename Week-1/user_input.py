f_num = input("Enter first number? ")

s_num = input("Enter second number?")

#convert the string into integer

sum_num = int(f_num) + int(s_num)

print("The sum of the two numbers is: ",sum_num)

#difference

diff_num = int(f_num) - int(s_num)

print("The difference of the two numbers is: ",diff_num)

#quotient

quot_num = int(f_num) / int(s_num)

print("The quotient of the two numbers is: ",quot_num)

#product

prod_num = int(f_num) * int(s_num)

print("The product of the two numbers is: ",prod_num)